package trains.models.tmt;

/**
 *
 * @author Ferdinand
 */
public class Vec3f {
    
    public float x;
    public float y;
    public float z;
    
    public Vec3f(){
        x = 0;
        y = 0;
        z = 0;
    }
    
    public Vec3f(float f, float g, float h){
        x = f;
        y = g;
        z = h;
    }
    
}
